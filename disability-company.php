            <h2>Mutual of Omaha Disability Denial Upheld by Appellate Court</h2>
            <p>In Giovanna Reichard v. United of Omaha Life Insurance Company (Mutual of Omaha or Omaha), Plaintiff,
                a nurse employed by a hospital, suffered from headaches, arthritis, Crohn’s disease, and fibromyalgia.
                She eventually had to quit work because of her illnesses and applied for long-term disability (LTD)
                benefits through her employer’s group-disability plan administered by Mutual of Omaha.</p>
            <p>Omaha granted her request for LTDs for two years during which time the plan considered a person disabled
                if they were unable to work in their own occupation. Omaha informed Plaintiff that in two years, the
                definition of disability would change, and she would have to prove she was unable to work in any
                occupation for which she was «‘reasonably fitted by training, education, or experience’ that would pay
                at least 60% of her pre-disability earnings within a year of going back to work».</p>
            <p>After two years, Omaha re-evaluated Plaintiff’s claim under the narrow definition of the policy. The
                company had her medical records reviewed by four professionals: a nurse, a vocational rehabilitation
                consultant, a physician, and a board-certified rheumatologist, who conducted an independent medical exam
                (IME). He also reviewed the reports of Omaha’s other medical record reviewers.</p>
            <p>After reviewing the four reports, Omaha denied Plaintiff’s claim, and she appealed. On the administrative
                appeal, Plaintiff objected on the grounds that Omaha:</p>
            <ul class="default-content-list">
                <li>Failed to consider the side effects of the medications she was taking.</li>
                <li>
                    Failed to consider some of her medical records.
                </li>
                <li>Used outdated records and incorrect information.</li>
            </ul>
